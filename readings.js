$(document).mousemove(function(){
    if ($("#responses:hover").length == 1){
        $("#responses").css("color", "rgb(53, 94, 57)")
    }
    else {
        $("#responses").css("color", "rgb(104, 43, 171)")
    }

})

$(document).mousemove(function(){
    if ($("#responses:hover").length ==1){
        $("#responses").css("filter", "none")
    }
    else {
        $("#responses").css("filter", "blur(5px)")
    }
})

